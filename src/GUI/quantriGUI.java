    public void loadListbacsy() {
        listModel = new DefaultListModel();
        listbacsi = daoo.getAlldatabacsy();
        jlistbs.removeAll();

        for (bacsy bs : listbacsi) {
            listModel.addElement(bs.getTenbacsy());
            System.out.println(bs.getTenbacsy());
        }
        jlistbs.setModel(listModel);
    }
