    public void LoadArrayTotable() {

        listpk = dao.getAlldatabenhnhanvachiphi(tenbsselect);
        model.setRowCount(0);
        float tongchiphi = 0;
        for (int i = 0; i < listpk.size(); i++) {
            boolean gt;

            Vector row = new Vector();
            row.add(listpk.get(i).getMabenhnhan());
            row.add(listpk.get(i).getTenbenhnhan());
            row.add(listpk.get(i).getChiphi());
            tongchiphi += listpk.get(i).getChiphi();
            model.addRow(row);
            tblbang.setModel(model);

        } // Handle any errors that may have occurred.
        txttongtien.setText(String.valueOf(tongchiphi));
    }

